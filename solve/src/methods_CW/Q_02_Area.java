package methods_CW;

public class Q_02_Area 
{
	void area(int a)
	{
		System.out.println("Area of Square : "+(a*a));
	}
	
	void area(int a, int b)
	{
		System.out.println("Area of Rectangle : "+(a*b));
	}
	
	void area(double half , int a, int b)
	{
		System.out.println("Area of Trangle : "+(half*a*b));
	}
	
	
}
