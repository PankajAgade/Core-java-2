package methods_HW;

public class Q_03_Calculator 
{
	double addition(int num1,int num2)
	{
		return num1 + num2;
	}
	
	double subtraction(int num1,int num2)
	{
		return num1 - num2;
	}
	double multiplication(int num1, int num2)
	{
		return num1 * num2;
	}
	
	double division(int num1, int num2)
	{
		return num1 / num2;
	}
}